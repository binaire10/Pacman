package fr.univ_amu.image;

public abstract class DimensionRasterImage implements RasterImage {
    private int width;
    private int height;

    public DimensionRasterImage(int width, int height) {
        this.width = width;
        this.height = height;
    }

    @Override
    public int getWidth() {
        return width;
    }

    @Override
    public void setWidth(int width) {
        this.width = width;
    }

    @Override
    public int getHeight() {
        return height;
    }

    @Override
    public void setHeight(int height) {
        this.height = height;
    }
}
